    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        return view('genre.create', [
            'genre' => Genre::find($id),
            'action' => route('genre.update', $id)
        ]);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $validated = $request->validate([
            'name' => 'required|string|max:100',
            'is_for_children' => 'sometimes|boolean'
        ]);

        $genre = Genre::find($id);
        $genre->setRawAttributes($validated);
        $genre->save();

        return redirect(route('genre.index'))->with('success', 'Saglabāts veiksmīgi!');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $genre = Genre::find($id);
        $genre->delete();

        return redirect(route('genre.index'))->with('success', 'Ieraksts izdzēsts!');
    }
}
